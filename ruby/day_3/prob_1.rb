# We need to know the length of string
# Check if x position > length of string.
# keep minusing length of string to get x position that's within the length of string. That's the new x position


# slope length == 3
# x = 10
# ..#.#...#O#  -> x == 9
# length = 11
# .#...##..#..#...##..#.   -> .#...##..#..X...##..#. --> x == 12
# x > length -> x = x - length == 1
#

def count_trees_hit(input)
  sled_pos = 0
  trees = 0
  input.each_with_index do |slope, index|
    next if index == 0
    sled_pos += 3
    sled_pos -= slope.length if sled_pos >= slope.length
    trees += 1 if slope.split("")[sled_pos] == "#"
  end

  trees
end

puts "count_trees: #{count_trees_hit(input)}"
